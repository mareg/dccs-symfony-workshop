<?php

namespace Mareg\Calculator;

class Calculator
{
    /**
     * @param number $a
     * @param number $b
     *
     * @return number
     */
    public function add($a, $b)
    {
        return $a + $b;
    }

    /**
     * @param number $a
     * @param number $b
     *
     * @return number
     */
    public function sub($a, $b)
    {
        return $a - $b;
    }
}